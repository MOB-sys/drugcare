export function getIngredientBySlug(slug: string): IngredientGuide | undefined {
  return ingredients.find((i) => i.slug === slug);
}

export function getAllIngredientSlugs(): string[] {
  return ingredients.map((i) => i.slug);
}

export function getRelatedIngredients(
  category: IngredientGuide["category"],
  excludeSlug: string,
  limit = 4,
): IngredientGuide[] {
  return ingredients
    .filter((i) => i.category === category && i.slug !== excludeSlug)
    .slice(0, limit);
}

export function getIngredientsByCategory(): Record<string, IngredientGuide[]> {
  const grouped: Record<string, IngredientGuide[]> = {};
  for (const cat of categoryOrder) {
    const items = ingredients.filter((i) => i.category === cat);
    if (items.length > 0) grouped[cat] = items;
  }
  return grouped;
}
